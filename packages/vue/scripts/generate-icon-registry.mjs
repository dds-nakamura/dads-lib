// Generates src/components/Icon/icon-registry.ts from @material-symbols/svg-400
// (outlined / weight 400). Only the icons actually used by @dads/vue are bundled
// (tree-shaking friendly). Re-run after changing MDI_TO_SYMBOL:
//   node scripts/generate-icon-registry.mjs
import { readFileSync, writeFileSync, existsSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, resolve } from 'node:path'

const __dirname = dirname(fileURLToPath(import.meta.url))
const PKG = resolve(__dirname, '..')
const SRC_DIR = resolve(PKG, 'node_modules/@material-symbols/svg-400/outlined')
const OUT = resolve(PKG, 'src/components/Icon/icon-registry.ts')

// mdi class name (legacy) -> Material Symbols name (new public icon name).
// This table also documents the migration mapping (see docs/quality/icon-mapping.md).
export const MDI_TO_SYMBOL = {
  'mdi-alert': 'warning',
  'mdi-alert-circle': 'error',
  'mdi-arrow-right': 'arrow_forward',
  'mdi-bell': 'notifications',
  'mdi-bookmark': 'bookmark',
  'mdi-calendar': 'calendar_today',
  'mdi-check': 'check',
  'mdi-check-circle': 'check_circle',
  'mdi-chevron-double-left': 'keyboard_double_arrow_left',
  'mdi-chevron-double-right': 'keyboard_double_arrow_right',
  'mdi-chevron-down': 'keyboard_arrow_down',
  'mdi-chevron-left': 'chevron_left',
  'mdi-chevron-right': 'chevron_right',
  'mdi-chevron-up': 'keyboard_arrow_up',
  'mdi-close': 'close',
  'mdi-close-circle': 'cancel',
  'mdi-cog': 'settings',
  'mdi-content-save': 'save',
  'mdi-download': 'download',
  'mdi-file': 'draft',
  'mdi-file-document': 'description',
  'mdi-fire': 'local_fire_department',
  'mdi-help-circle-outline': 'help',
  'mdi-home': 'home',
  'mdi-information': 'info',
  'mdi-magnify': 'search',
  'mdi-menu': 'menu',
  'mdi-newspaper': 'newspaper',
  'mdi-open-in-new': 'open_in_new',
  'mdi-tag': 'label',
}

// Extra symbols bundled for consumers/demos (e.g. sample-app) that are not used
// directly inside @dads/vue components.
export const EXTRA_SYMBOLS = ['apps', 'logout', 'dashboard', 'account_circle']

const symbols = [...new Set([...Object.values(MDI_TO_SYMBOL), ...EXTRA_SYMBOLS])].sort()

const extract = (svg) => {
  const viewBox = svg.match(/viewBox="([^"]+)"/)?.[1] ?? '0 -960 960 960'
  // inner content between the opening <svg ...> and closing </svg>
  const body = svg.replace(/^[\s\S]*?<svg[^>]*>/, '').replace(/<\/svg>\s*$/, '').trim()
  return { viewBox, body }
}

const entries = []
const missing = []
for (const name of symbols) {
  const file = resolve(SRC_DIR, `${name}.svg`)
  if (!existsSync(file)) {
    missing.push(name)
    continue
  }
  const { viewBox, body } = extract(readFileSync(file, 'utf8'))
  entries.push({ name, viewBox, body })
}

if (missing.length) {
  console.error('[generate-icon-registry] missing Material Symbols:', missing.join(', '))
  process.exit(1)
}

const header = `// AUTO-GENERATED by scripts/generate-icon-registry.mjs — do not edit by hand.
// Source: @material-symbols/svg-400 (outlined, weight 400, Apache-2.0).
// Only icons used by @dads/vue are bundled. Run the script to regenerate.

export interface DadsIconDef {
  /** SVG viewBox of the source icon (Material Symbols use a 960 grid). */
  viewBox: string
  /** Inner SVG markup (path/etc.); inherits \`fill: currentColor\` from <svg>. */
  body: string
}

export const iconRegistry: Record<string, DadsIconDef> = {
`
const rows = entries
  .map((e) => `  ${JSON.stringify(e.name)}: ${JSON.stringify({ viewBox: e.viewBox, body: e.body })},`)
  .join('\n')
const footer = `
}

/** Material Symbols names bundled in the registry. */
export type DadsIconName = keyof typeof iconRegistry
`

writeFileSync(OUT, header + rows + footer + '\n')
console.log(`[generate-icon-registry] wrote ${entries.length} icons -> ${OUT}`)
